namespace 装置
{
    // 装置主流程阶段，由主按钮点击和机关事件驱动转换
    public enum 游戏阶段
    {
        未开始,     // 开局：只有主按钮，提示别点
        启动,       // 点了主按钮，效果界面开始基础判定
        密码盘,     // 主按钮点够次数，密码盘出现定曲风
        曲风确定,   // 密码盘提交正确曲风
        谱面互动,   // 效果界面出特殊判定+新谱面，按谱面点主按钮
        机关解锁,   // 主按钮互动够次数，多个机关面板出现
        机关操作,   // 玩家操作各机关
        结局        // 全部机关成功，进入结局演出
    }
}
